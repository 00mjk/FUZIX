static const uint8_t *run_conditions(const uint8_t *p, uint8_t n)
{
  uint8_t i;
  
  for (i = 0; i < n; i++) {
    uint8_t opc = *p++;
    uint16_t par = *p++ | ((opc & 0xE0) >> 5);
    uint8_t op = objloc[par];
    opc &= 0x1F;

    switch(opc) {
      case 0:
        *param++ = par;
        break;
      case 1:
        if (op != 255)
          return NULL;
        break;
      case 2:
        if (op != location)
          return NULL;
        break;
      case 3:
        if (op != 255 && op != location)
          return NULL;
        break;
      case 4:
        if (location != par)
          return NULL;
        break;
      case 5:
        if (op == location)
          return NULL;
        break;
      case 6:
        if (op == 255)
          return NULL;
        break;
      case 7:
        if (location == par)
          return NULL;
        break;
      case 8:
        if (!(bitflags & (1 << par)))
          return NULL;
        break;
      case 9:
        if (bitflags & (1 << par))
          return NULL;
        break;
      case 10:
        if (!carried)
          return NULL;
        break;
      case 11:
        if (carried)
          return NULL;
        break;
      case 12:
        if (op == 255 || op == location)
          return NULL;
        break;
      case 13:
        if (op == 0)
          return NULL;
        break;
      case 14:
        if (op != 0)
          return NULL;
        break;
      case 15:
        if (counter > par)
          return NULL;
        break;
      case 16:
        if (counter < par)
          return NULL;
        break;
      case 17:
        if (op != objinit[par]) 
          return NULL;
        break;
      case 18:
        if (op == objinit[par])
          return NULL;
        break;
      case 19:
        if (counter != par)
          return NULL;
        break;
      default:
        error("BADCOND");
    }
  }
  return p;
}

uint8_t islight(void)
{
  uint8_t l = objloc[LIGHT_SOURCE];
  if (!(bitflags & (1 << DARKFLAG)))
    return 1;
  if (l == 255 || l == location)
    return 1;
  return 0;
}

static void action_look(void)
{
  const uint8_t *e;
  const uint8_t *p;
  uint8_t c;
  uint8_t f = 1;
  const uint8_t **op = objtext;

  redraw = 0;

  begin_upper();

  if (!islight()) {
    strout_upper(itsdark);
    end_upper();
    return;
  }
  p = locdata[location].text;
  e = locdata[location].exit;
  if (*p == '*')
    p++;
  else
    strout_upper(youare);
  strout_upper(p);
  strout_upper(newline);
  strout_upper(obexit);

  for (c = 0; c < 6; c++) {
    if (*e++) {
      if (f)
        f = 0;
      else
        strout_upper(dashstr);
      strout_upper(exitmsgptr[c]);
    }
  }
  if (f)
    strout_upper(nonestr);
  strout_upper(dotnewline);
  f = 1;
  e = objloc;
  while(e < objloc + NUM_OBJ) {
    if (*e++ == location) {
      if (f) {
        strout_upper(canalsosee);
        f = 0;
      } else
        strout_upper(dashstr);
      strout_upper(*op);
    }
    op++;
  }
  end_upper();
}

