static void run_actions(const uint8_t *p, uint8_t n)
{
  uint8_t i;

  for (i = 0; i < n; i++) {
    uint8_t a = *p++;
    uint8_t tmp;
    uint16_t tmp16;

    if (a < 50) {
      strout_lower_spc(msgptr[a]);
      continue;
    }
    if (a > 102 ) {
      strout_lower_spc(msgptr[a - 50]);
      continue;
    }
    switch(a) {
      case 51:	/* nop - check */
        break;
      case 52:	/* Get */
        if (carried >= maxcar)
          strout_lower(toomuch);
        else
          moveitem(*param++, 255);
        break;
      case 53: /* Drop */
        moveitem(*param++, location);
        break;
      case 54: /* Go */
        location = *param++;
        redraw = REDRAW;
        break;
      case 55: /* Destroy */
      case 59: /* ?? */
        moveitem(*param++, 0);
        break;
      case 56:	/* Set dark flag */
        bitflags |= (1 << DARKFLAG);
        break;
      case 57:	/* Clear dark flag */
        bitflags &= ~(1 << DARKFLAG);
        break;
      case 58:	/* Set bit */
        bitflags |= (1 << *param++);
        break;
      /* 59 see 55 */
      case 60:	/* Clear bit */
        bitflags &= ~(1 << *param++);
        break;
      case 61:	/* Dead */
        action_dead();
        break;
      case 64:	/* Look */
      case 76:	/* Also Look ?? */
        action_look();
        break;
      case 62:	/* Place obj, loc */
        tmp = *param++;
        moveitem(tmp, *param++);
        break;
      case 63:	/* Game over */
        action_quit();
      case 65:	/* Score */
        action_score();
        break;
      case 66:	/* Inventory */
        action_inventory();
      case 67:	/* Set bit 0 */
        bitflags |= (1 << 0);
        break;
      case 68:	/* Clear bit 0 */
        bitflags &= ~(1 << 0);
        break;
      case 69:	/* Refill lamp */
        lighttime = lightfill;
        bitflags &= ~(1 << LIGHTOUT);
        moveitem(LIGHT_SOURCE, 255);
        break;
      case 70:	/* Wipe lower */
        /* TODO */
        break;
      case 71:	/* Save */
        /* TODO */
      case 72:	/* Swap two objects */
        tmp = objloc[*param];
        moveitem(*param, objloc[param[1]]);
        moveitem(param[1], tmp);
        param += 2;
        break;
      case 73:
        continuation = 1;
        break;
      case 74:	/* Get without weight rule */
        moveitem(*param++, 255);
        break;
      case 75:	/* Put one item by another */
        moveitem(*param, objloc[param[1]]);
        param += 2;
        break;
      case 77:	/* Decrement counter */
        if (counter >= 0)
          counter--;
        break;
      case 78:	/* Display counter */
        decout_lower(counter);
        break;
      case 79:	/* Set counter */
        counter = *param++;
        break;
      case 80:	/* Swap player and saved room */
        tmp = savedroom;
        savedroom = location;
        location = tmp;
        redraw = REDRAW;
        break;
      case 81:	/* Swap counter and counter n */
        tmp16 = counter;
        counter = counter_array[*param];
        counter_array[*param++] = tmp16;
        break;
      case 82:	/* Add to counter */
        counter += *param++;
        break;
      case 83:	/* Subtract from counter */
        counter -= *param++;
        if (counter < 0)
          counter = -1;
        break;
      case 84:	/* Print noun, newline */
        strout_lower((uint8_t *)nounbuf);
        /* Fall through */
      case 86:	/* Print newline */
        strout_lower(newline);
        break;
      case 85:	/* Print noun */ 
        strout_lower((uint8_t *)nounbuf);
        break;
      case 87: /* Swap player and saveroom array entry */
        tmp16 = *param++;
        tmp = roomsave[tmp16];
        roomsave[tmp16] = location;
        if (tmp != location) {
          location = tmp;
          redraw = REDRAW;
        }
        break;
      case 88:
        action_delay();
        break;
      case 89:
        param++;		/* SAGA etc specials */
        break;
      default:
        error("BADACT");
    }
  }
}

void next_line(void)
{
  uint8_t c = *linestart++;
  if (!(c & 0x80))
    linestart += 2;	/* Skip verb/noun */
  else if (!(c & 0x60))
    linestart++;	/* Skip random value */
  linestart += (c & 3) + 1;	/* Actions 1 - 4 */
  c >>= 1;
  c &= 0x0E;		/* 2 x conditions */
  linestart += c;
}

void run_line(const uint8_t *ptr, uint8_t c, uint8_t a)
{
  memset(param_buf, 0, sizeof(param_buf));
  param = param_buf;
  if (c)
    ptr = run_conditions(ptr, c);
  if (ptr) {
    actmatch = 1;
    param = param_buf;
    run_actions(ptr, a);
  }
  next_line();
}

void run_table(const uint8_t *tp)
{
  continuation = 0;
  linestart = tp;
  while(1) {
    uint8_t hdr;
    uint8_t c, a;
    tp = linestart;
    hdr = *tp++;
    c = (hdr >> 2) & 0x07;
    a = (hdr & 3) + 1;
    
/*    printf("H%02X c = %d a = %d\n", hdr, c, a); */
    if (hdr == 255)
      return;		/* End of table */
    if (hdr & 0x80) {
      if (hdr & 0x40) {	/* Auto 0 */
        if (continuation)
          run_line(tp, c, a);
        else
          next_line();
        continue;
      }
      continuation = 0;
      if (!(hdr & 0x20)) {	/* Auto number */
        if (random_chance(*tp++))
          run_line(tp, c, a);
        else
          next_line();
        continue;
      }
      run_line(tp, c, a);
    } else {
      if (actmatch)
        return;
/*      printf("VN %d %d\n", *tp, tp[1]); */
      linematch = 1;
      continuation = 0;
      if (*tp++ == verb && (*tp == noun || *tp == 0))
        run_line(tp+1, c, a);
      else
        next_line();
    }
  }
}

uint8_t autonoun(uint8_t loc)
{
  const uint8_t *p = automap;
  if (*wordbuf == ' ' || *wordbuf == 0)
    return 255;
  while(*p) {
    if (strncasecmp((const char *)p, wordbuf, WORDSIZE) == 0 && objloc[p[WORDSIZE]] == loc)
      return p[WORDSIZE];
    p += WORDSIZE + 1;
  }
  return 255;
}
  
void run_command(void)
{
  uint8_t tmp;
  run_table(actions);
  if (actmatch)
    return;
  if (verb == VERB_GET) {		/* Get */
    if (noun == 0)
      strout_lower(whatstr);
    else if (carried >= maxcar)
      strout_lower(toomuch);
    else {
      tmp = autonoun(location);
      if (tmp == 255)
        strout_lower(beyondpower);
      else
        moveitem(tmp, 255);
    }
    actmatch = 1;
    return;
  }
  if (verb == VERB_DROP) {		/* Drop */
    if (noun == 0)
      strout_lower(whatstr);
    else {
      tmp = autonoun(255);
      if (tmp == 255)
        strout_lower(beyondpower);
      else
        moveitem(tmp, location);
    }
    actmatch = 1;
    return;
  }
}

void process_light(void)
{
  uint8_t l;
  if ((l = objloc[LIGHT_SOURCE]) == 0)
    return;
  if (lighttime == 255)
    return;
  if (!--lighttime) {
    bitflags &= ~(1 << LIGHTOUT);	/* Check clear ! */
    if (l == 255 || l == location) {
      strout_lower(lightout);
      redraw = REDRAW_MAYBE;
      return;
    }
  }
  if (lighttime > 25)
    return;
  strout_lower(lightoutin);
  decout_lower(lighttime);
  strout_lower(lighttime == 1 ? turn : turns);
}

void main_loop(void)
{
  uint8_t first = 1;
  char *p;

  action_look();
  
  while (1) {
    if (!first)
      process_light();
    else
      first = 0;
    verb = 0;
    noun = 0;
    run_table(status);

    if (redraw & REDRAW_MASK)
      action_look();

    strout_lower(whattodo);
    do {
      do {
        strout_lower(prompt);
        line_input();
        abbrevs();
        p = skip_spaces(linebuf);
      }
      while(*p == 0);

      scan_noun(p);
      if (noun && noun <= 6) {
        verb = VERB_GO;
        break;
      }
      scan_input();
      if (verb == 255)
        strout_lower(dontknow);
    } while (verb == 255);
    
    if (verb == VERB_GO) {
      if (!noun) {
        strout_lower(givedirn);
        continue;
      }
      if (noun <= 6) {
        uint8_t light = islight();
        uint8_t dir;

        if (!light)
          strout_lower(darkdanger);
        dir = locdata[location].exit[noun - 1];
        if (!dir) {
          if (!light) {
            strout_lower(brokeneck);
            action_delay();
            action_dead();
            continue;
          }
          strout_lower(cantgo);
          continue;
        }
        location = dir;
        redraw = REDRAW;
        continue;
      }
    }
    linematch = 0;
    actmatch = 0;
    run_command();
    if (actmatch)
      continue;
    if (linematch) {
      strout_lower(notyet);
      continue;
    }
    strout_lower(dontunderstand);
  }
}

void start_game(void)
{
  memcpy(objloc, objinit, sizeof(objloc));
  bitflags = 0;
  counter = 0;
  memset(counter_array, 0, sizeof(counter_array));
  savedroom = 0;
  memset(roomsave, 0, sizeof(roomsave));
  location = startloc;
  lighttime = startlamp;
  carried = startcarried;
}

int main(int argc, char *argv[])
{
  display_init();
  setjmp(restart);
  start_game();
  main_loop();
}
